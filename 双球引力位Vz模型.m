%%同双球模型：y向100m，一个在30，一个在60 其他参数都一样：
R1=1;R2=2.5; h1=5;h2=8 %球半径为1米，深为5米
p=0.1; %剩余密度
m1=4/3*pi*R1^3*p; %剩余质量
m2=4/3*pi*R2^3*p; %剩余质量
f=6; %引力常数 
%%半径r的相关参数:
x0=0;y0=30;y1=50; % 唯独这里y0与y1是有变化的
x=0;z=0; %% h与z两个球都是一样的
%%
for y=0:0.1:100
    r1=((x-x0)^2+(y-y0)^2+(h1-z)^2)^(0.5);
    r2=((x-x0)^2+(y-y1)^2+(h2-z)^2)^(0.5);
    Vz=f*m1*(h1-z)/(r1^3)+f*m2*(h2-z)/(r2^3);
    plot(y,Vz,'r');
    hold on 
end