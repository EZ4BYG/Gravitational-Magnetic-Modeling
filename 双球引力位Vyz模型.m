%%同双球模型：y向100m，一个在30，一个在60 其他参数都一样：
R=1; h=5; %球半径为1米，深为5米
p=0.1; %剩余密度
m=4/3*pi*R^3*p; %剩余质量
f=6; %引力常数 
%%半径r的相关参数:
x0=0; y0=30;y1=60; % 唯独这里y0与y1是有变化的
x=0;z=0; %% h与z两个球都是一样的
%%
for y=0:0.1:100
    r1=((x-x0)^2+(y-y0)^2+(h-z)^2)^(0.5);
    r2=((x-x0)^2+(y-y1)^2+(h-z)^2)^(0.5);
    Vyz0=-3*f*m*(y-y0)*(h-z)/r1^5;
    Vyz1=-3*f*m*(y-y1)*(h-z)/r2^5;
    Vyz=Vyz0+Vyz1
    plot(y,Vyz,'r');  
    hold on 
end